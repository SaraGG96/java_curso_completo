public class Variables {
    public static void main(String[] args) {
        
        String name = "Sara";
        System.out.println(name);

        int age = 29;
        System.out.println(age);

        name = "SaraGG96";
        System.out.println(name);

        var email = "gmailvariable@gmail.com";
        System.out.println(email);
        // No se define el tipo, pero al ir entre 
        // "" Java entiende que es string

        var year = 2026;
        System.out.println(year);
        // Aquí entiende que es un int

    }
}
